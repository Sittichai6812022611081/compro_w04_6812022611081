#include <stdio.h>                                          //การนำเข้าไลบรารีสำหรับการใช้งานฟังก์ชัน printf
void countCall() {
    int counter = 0;                                        // ประกาศตัวแปร counter ในระดับ local ของฟังก์ชัน
    counter++;                                              // เพิ่มค่า counter ทุกครั้งที่ฟังก์ชันถูกเรียก
    printf("Call Function: Counter = %d\n", counter);       // แสดงค่าของ counter
    
}

int main() {                                                // ฟังก์ชันหลักของโปรแกรม
    printf("Starting function calls...\n");                 // แสดงข้อความเริ่มต้น
    countCall();                                            // เรียกใช้ฟังก์ชัน countCall ครั้งแรก
    printf("After first call;\n");                          // แสดงข้อความหลังจากเรียกใช้ฟังก์ชันครั้งแรก
    countCall();                                            // เรียกใช้ฟังก์ชัน countCall ครั้งที่สอง
    return 0;                                               // ส่งค่ากลับ 0 เพื่อบ่งบอกว่าการทำงานสำเร็จ
}

// 1.ผลลัพธ์ที่ได้ถ้าเป็น int เมื่อเรียกใช้ฟังก์ชัน countCall() จะทำให้ตัวแปร counter ถูกรีเซ็ตเป็น 0 ทุกครั้งที่ฟังก์ชันถูกเรียก
// ทำให้ผลลัพธ์แสดงจำนวนครั้งที่ถูกเรียกใช้เป็น 1 เสมอ

//2.ความแตกต่างในการใช้งานของ Static และ Non-Static Variable
// ตัวแปร static จะเก็บค่าระหว่างการเรียกใช้ฟังก์ชัน ทำให้สามารถนับจำนวนครั้งที่ถูกเรียกใช้ได้
// ในขณะที่ตัวแปร non-static จะถูกรีเซ็ตทุกครั้งที่ฟังก์ชันถูกเรียก ทำให้ไม่สามารถนับจำนวนครั้งที่ถูกเรียกใช้ได้

//3.อภิปราย และสรุปผลที่ได้จากการทดลอง
// การทดลองนี้แสดงให้เห็นถึงความแตกต่างระหว่างตัวแปร static และ non-static ในการเก็บค่าระหว่างการเรียกใช้ฟังก์ชัน
// การใช้ตัวแปร static ทำให้สามารถเก็บค่าจำนวนครั้งที่ถูกเรียกใช้ได้อย่างต่อเนื่อง
// ในขณะที่ตัวแปร non-static จะไม่สามารถเก็บค่าระหว่างการเรียกใช้ฟังก์ชันได้