#include <stdio.h>                                        //การนำเข้าไลบรารีสำหรับการใช้งานฟังก์ชัน printf

const int GLOBAL_RATE = 10;                               // ค่าคงที่ระดับ global

void calculate() {                                        // ฟังก์ชันที่ใช้คำนวณ
    const int LOCAL_BONUS = 50;                           // ค่าคงที่ระดับ local ที่ประกาศในฟังก์ชัน calculate
    printf("In calculate():\n");                          // แสดงข้อความเมื่อเข้าไปในฟังก์ชัน calculate
    printf("GLOBAL_RATE = %d\n", GLOBAL_RATE);            // แสดงค่าคงที่ระดับ global
    printf("LOCAL_BONUS = %d\n", LOCAL_BONUS);            // แสดงค่าคงที่ระดับ local
}

int main() {                                              // ฟังก์ชันหลักของโปรแกรม
    calculate();                                          // เรียกใช้ฟังก์ชัน calculate เพื่อแสดงค่าคงที่
    printf("\nIn main():\n");                             // แสดงข้อความเมื่อเข้าไปในฟังก์ชัน main
    printf("GLOBAL_RATE = %d\n", GLOBAL_RATE);            // แสดงค่าคงที่ระดับ global
    //printf("LOCAL_BONUS in main = %d\n", LOCAL_BONUS); จะเกิดข้อผิดพลาดที่นี่ เนื่องจาก LOCAL_BONUS ไม่สามารถเข้าถึงได้ใน main
    return 0;                                            // ส่งค่ากลับ 0 เพื่อบ่งบอกว่าการทำงานสำเร็จ
}

// คำถามท้ายโจทย์:
//1.ค่าคงที่GLOBAL_RATE และ LOCAL_BONUS ต่างกันอย่างไร
// GLOBAL_RATE เป็นค่าคงที่ที่ประกาศในระดับ global ซึ่งสามารถเข้าถึงได้จากทุกฟังก์ชันในโปรแกรม
//ในขณะที่ LOCAL_BONUS เป็นค่าคงที่ที่ประกาศในระดับ local ของฟังก์ชัน calculate() ซึ่งไม่สามารถเข้าถึงได้จากนอกฟังก์ชันนั้น

//2.เกิดอะไรขึ้นถ้าเราเขียน GLOBAL_RATE = 20; หรือ LOCAL_BONUS = 80;?
// ถ้าเราเขียน GLOBAL_RATE = 20; หรือ LOCAL_BONUS = 80; จะเกิดข้อผิดพลาดในการคอมไพล์ เพราะค่าคงที่ (const) ไม่สามารถถูกเปลี่ยนแปลงได้หลังจากที่ถูกประกาศแล้ว

//3.ทำไม Local Bonus ไม่สามารถเข้าถึงได้ใน main()ได้
//เนื่องจาก LOCAL_BONUS ถูกประกาศในฟังก์ชัน calculate() ซึ่งเป็นตัวแปรที่มีขอบเขตเฉพาะในฟังก์ชันนั้น ดังนั้นมันจึงไม่สามารถเข้าถึงได้จากฟังก์ชัน main

//4.อภิปราย และสรุปผลที่ได้จากการทดลอง
// การทดลองนี้แสดงให้เห็นถึงความแตกต่างระหว่างค่าคงที่ระดับ global และ local ค่าคงที่ระดับ global สามารถเข้าถึงได้จากทุกฟังก์ชันในโปรแกรม
//ในขณะที่ค่าคงที่ระดับ local จะมีขอบเขตการเข้าถึงที่จำกัดอยู่ภายในฟังก์ชันที่ประกาศเท่านั้น