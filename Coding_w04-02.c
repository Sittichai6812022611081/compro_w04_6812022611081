#include <stdio.h>              // นำเข้าไลบรารีสำหรับการใช้งานฟังก์ชัน printf

int main() {                    //เริ่มต้นโปรแกรมหลัก
    
    int a = 10 ;                // ประกาศตัวแปร a และกำหนดค่าเริ่มต้นเป็น 10 (Identifier)
    printf("a = %d", a);        // แสดงค่าของตัวแปร a ในรูปแบบจำนวนเต็ม (Format specifier %d)
    return 0 ;                  //จบโปรแกรมหลักและส่งค่ากลับ 0
}